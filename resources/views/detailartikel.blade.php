        <div class="isi-artikel font-artikel">
            <p>Indonesia adalah suatu negara yang mempunyai banyak tempat menyelam terbaik di dunia. Bahkan dinobatkan sebagai diving spot terbaik di dunia karena keindahan bawah lautnya, Para penyelam dari pelosok negeri  hingga penjuru dunia selalu datang berkunjung setiap tahunnya. Jadi, Anda para penggemar olahraga air kayaknya kurang lengkap kalau belum menjajal scuba diving alias menyelam. Kalau Anda ingin berpetualang di bawah air bersama makhluk-makhluk fantastis, ini dia 10 pantai terbaik buat diving di laut yang ada di Indonesia.</p>
