            <p class="mb-20">Kepulauan Banda terletak di Maluku, tepatnya 132 km sebelah tenggara Ambon. 
                Terdiri dari 10 pulau dengan 3 pulau besar (Banda Besar, Banda, Neira) dan 7 pulau kecil. 
                Kepulauan ini memiliki taman laut yang  fantastis sehingga daerah ini menjadi primadoa bagi penyelam.

            <br><br>Lebih dari 20 spot untuk menyelam yang berada di Kepulauan Banda. 
            Dari sekian banyak spot yang ada, spot yang paling terkenal adalah Pulau Keraka Sonegat, 
            Pulau Syahrir Batu Kapal, Pulau Hatta, serta Pulau Ai. Bila Anda ingin pergi Kepulauan Banda. 
            Anda bisa pergi ke Ambon menggunakan Pesawat kemudian baru naik kapal ke Kepulauan Banda.</p>

